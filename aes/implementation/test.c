int check_equal(const uint8_t *a, const uint8_t *b, int len, const char *name) {
    if (memcmp(a, b, len) == 0) {
        printf("  [PASS] %s\n", name);
        return 1;
    } else {
        printf("  [FAIL] %s\n", name);
        printf("    期望: ");
        for (int i = 0; i < len; i++) printf("%02x", b[i]);
        printf("\n    实际: ");
        for (int i = 0; i < len; i++) printf("%02x", a[i]);
        printf("\n");
        return 0;
    }
}

// 辅助：运行一个加密测试
int run_test(const char *name, const uint8_t *key, const uint8_t *pt, const uint8_t *expected) {
    uint8_t rk[176];
    key_expansion(key, rk, 4);
    uint8_t *ct = encrypt(pt, rk, 10);
    int result = check_equal(ct, expected, 16, name);
    free(ct);
    return result;
}

